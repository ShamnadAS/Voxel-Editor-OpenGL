#include <engine/cubeRenderer.h>

CubeRenderer::CubeRenderer(Shader &shader)
{
    this->shader = shader;
	this->Init();
}
CubeRenderer::~CubeRenderer()
{
    glDeleteVertexArrays(1, &this->VAO);
}
